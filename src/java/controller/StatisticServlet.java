    @Override
    protected void doGet(HttpServletRequest request, HttpServletResponse response)
            throws ServletException, IOException {
        AdminDAO ad = new AdminDAO();
        DAO d = new DAO();
        List<Order> list = ad.getOrder();
        Double total = 0.0;
        for (int i = 0; i < list.size(); i++) {
            total = total + list.get(i).getTotalMoney();
        }
//        List<OrderItems> list_o = ad.getOrderItems();
        String id=ad.getCustomerBuyMost();
        String product=ad.getProductBuyMost();
        Product p = d.getProductByID(product);
        request.setAttribute("pro", p);
        request.setAttribute("cusid", id);
        request.setAttribute("product", product);
        request.setAttribute("total", total);
        request.setAttribute("data", list);
        request.getRequestDispatcher("thongke.jsp").forward(request, response);
    }

